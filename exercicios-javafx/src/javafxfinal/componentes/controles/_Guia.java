package javafxfinal.componentes.controles;

public class _Guia {

    /*
     * CheckBox:
     * Representa uma opção que pode ser ativada ou desativada. Ideal para
     * permitir ao usuário selecionar múltiplas opções independentes.
     */

    /*
     * ChoiceBox:
     * Exibe um menu suspenso com um número fixo de opções. Útil para permitir
     * ao usuário escolher uma única opção de um conjunto limitado.
     */

    /*
     * ColorPicker:
     * Permite ao usuário selecionar uma cor a partir de uma paleta de cores.
     * Ideal para aplicações que requerem customização de cores.
     */

    /*
     * ComboBox:
     * Combina um campo de texto editável com uma lista suspensa. Útil para
     * permitir ao usuário selecionar uma opção de uma lista ou digitar sua própria.
     */

    /*
     * DatePicker:
     * Permite ao usuário selecionar uma data a partir de um calendário.
     * Ideal para entradas de datas, como agendamento de eventos.
     */

    /*
     * Hyperlink:
     * Representa um link que pode ser clicado, geralmente para abrir uma
     * URL. Útil para navegação e referências externas.
     */

    /*
     * ListView:
     * Exibe uma lista de itens rolável. Ideal para mostrar um conjunto de
     * itens onde o usuário pode selecionar um ou múltiplos itens.
     */

    /*
     * PasswordField:
     * Um campo de texto especializado para entrada de senhas, onde o texto
     * digitado é mascarado. Útil para inputs de senhas e informações sensíveis.
     */

    /*
     * ProgressBar:
     * Exibe um indicador de progresso horizontal. Ideal para mostrar o
     * progresso de uma operação em andamento.
     */

    /*
     * ProgressIndicator:
     * Exibe um indicador de progresso circular. Útil para mostrar o progresso
     * de uma operação em andamento, especialmente em espaços limitados.
     */

    /*
     * RadioButton:
     * Representa uma opção que pode ser selecionada ou deselecionada,
     * geralmente usada em grupos onde apenas uma opção pode ser selecionada.
     * Ideal para opções mutuamente exclusivas.
     */

    /*
     * Slider:
     * Permite ao usuário selecionar um valor de um intervalo contínuo
     * deslizando um controle. Útil para ajustes finos como volume ou brilho.
     */

    /*
     * Spinner:
     * Permite ao usuário selecionar um valor de um intervalo por meio de
     * botões de incremento e decremento. Ideal para entradas numéricas.
     */

    /*
     * TableView:
     * Exibe dados em uma tabela com linhas e colunas. Útil para apresentar
     * grandes conjuntos de dados estruturados.
     */

    /*
     * TextArea:
     * Um campo de texto multi-linha que permite ao usuário digitar texto
     * longo. Ideal para comentários, descrições ou mensagens.
     */

    /*
     * TextField:
     * Um campo de texto de linha única para entrada de texto curto. Útil
     * para nomes, endereços de e-mail, etc.
     */

    /*
     * ToggleButton:
     * Um botão que pode estar ativado ou desativado, semelhante a um CheckBox,
     * mas visualmente similar a um botão. Útil para alternar estados binários.
     */

    /*
     * TreeView:
     * Exibe uma estrutura hierárquica de itens, como um diretório de arquivos.
     * Ideal para representar dados em forma de árvore.
     */
}
