package javafxfinal.componentes.interatividade;

/**
 * Classe GuiaInteratividade
 * 
 * Esta classe fornece uma visão geral das funcionalidades disponíveis nas classes do pacote
 * interatividade: Alert, Dialog, EventHandler e Tooltip.
 */
public class _Guia {

    /**
     * Alert
     * 
     * A classe Alert é utilizada para exibir mensagens ao usuário em uma janela separada. 
     * É ideal para mostrar informações, avisos ou erros. O Alert pode ter diferentes tipos,
     * como informação, aviso e erro, permitindo que o desenvolvedor escolha a melhor 
     * apresentação de acordo com o contexto.
     */

    /**
     * Dialog
     * 
     * A classe Dialog é utilizada para solicitar entrada do usuário ou exibir informações.
     * Um exemplo comum é o TextInputDialog, que permite capturar texto do usuário. 
     * É útil quando é necessário obter informações diretamente do usuário antes de prosseguir.
     */

    /**
     * EventHandler
     * 
     * A classe EventHandler é uma interface usada para tratar eventos, como cliques de 
     * botões. Com ela, você pode definir ações personalizadas que devem ocorrer quando 
     * eventos específicos são acionados. Por exemplo, ao clicar em um botão, você pode 
     * mudar a cor dele ou executar outras ações relevantes.
     */

    /**
     * Tooltip
     * 
     * A classe Tooltip é utilizada para fornecer informações adicionais sobre um 
     * componente quando o mouse passa sobre ele. Isso é útil para melhorar a 
     * usabilidade, permitindo que os usuários entendam melhor a função de um botão 
     * ou outro controle sem sobrecarregar a interface com texto.
     */

    // Você pode adicionar outros métodos ou atributos conforme necessário
}
