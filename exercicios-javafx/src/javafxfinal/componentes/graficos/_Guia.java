package javafxfinal.componentes.graficos;

public class _Guia {

    /*
     * Canvas: 
     * Utilizado para desenhar gráficos 2D, permitindo que você crie desenhos, 
     * formas e imagens. É a base para a renderização gráfica em JavaFX.
     * Ideal para aplicações que requerem desenhos dinâmicos e interativos.
     */
    
    /*
     * ImageView: 
     * Classe usada para exibir imagens em uma interface gráfica. 
     * Aceita diversos formatos de imagem e é útil para carregar e mostrar 
     * imagens estáticas em aplicações.
     */
    
    /*
     * MediaView: 
     * Usada para reproduzir mídias, como vídeos ou animações. 
     * É adequada para aplicações que necessitam de suporte a reprodução 
     * de conteúdo multimídia.
     */
    
    /*
     * Path: 
     * Representa uma sequência de comandos de desenho, permitindo criar 
     * formas complexas através de curvas e linhas. É útil para 
     * gráficos vetoriais e desenhos personalizados.
     */
    
    /*
     * Shape: 
     * Classe base para todos os objetos de forma em JavaFX. 
     * Serve como ponto de partida para a criação de formas geométricas 
     * e personalizadas, como círculos, retângulos e polígonos.
     */
    
    /*
     * WebView: 
     * Permite a integração de conteúdo web em aplicações JavaFX. 
     * Pode ser usado para mostrar páginas da web, interagir com conteúdo HTML e executar scripts.
     * É ideal para aplicações que requerem funcionalidades de navegador.
     */
}
