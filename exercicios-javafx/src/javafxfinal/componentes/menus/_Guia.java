package javafxfinal.componentes.menus;

/**
 * Componentes de Menu - Guia
 * Esta classe fornece uma descrição breve de cada um dos componentes de menu utilizados no JavaFX.
 * 
 * 1. MenuBar:
 * O MenuBar é o componente principal para criar menus na interface do usuário. 
 * Ele permite agrupar itens de menu (Menus) e é frequentemente utilizado em aplicações desktop 
 * para organizar ações comuns, como Arquivo, Editar e Ajuda. A MenuBar é posicionada geralmente na parte superior da janela.
 * 
 * 2. CheckMenuItem:
 * O CheckMenuItem permite que o usuário selecione ou deselecione uma opção em um menu. 
 * É útil para configurações onde múltiplas opções podem ser ativadas ao mesmo tempo, 
 * como em um menu de preferências ou opções de exibição. Os CheckMenuItems são representados com uma caixa de seleção.
 * 
 * 3. ToolBar:
 * A ToolBar é um componente que contém botões e outros controles em uma barra horizontal ou vertical. 
 * É ideal para fornecer acesso rápido a ações comuns, como salvar, abrir ou editar. As ToolBars podem incluir ícones e são frequentemente 
 * utilizadas em aplicações que requerem uma interface rápida e intuitiva.
 * 
 * 4. RadioMenuItem:
 * O RadioMenuItem é usado em um ToggleGroup, permitindo que o usuário selecione apenas uma opção de um conjunto. 
 * É ideal para cenários onde a escolha é mutuamente exclusiva, como selecionar um formato ou modo de operação. 
 * Os RadioMenuItems são visualmente representados como opções de menu que excluem a seleção de outras opções.
 * 
 * 5. ContextMenu:
 * O ContextMenu é um menu que aparece ao clicar com o botão direito do mouse em um componente ou área da interface. 
 * É usado para fornecer opções relevantes de maneira contextual, como copiar, colar, ou ações específicas relacionadas ao item em que foi clicado. 
 * O uso do ContextMenu melhora a usabilidade ao apresentar opções relevantes de forma imediata.
 */
