package javafxfinal.componentes.paineis.paineis;

public class _Guia {

    /*
     * SplitPane: 
     * Permite dividir a área de visualização em duas ou mais seções que podem 
     * ser ajustadas pelo usuário. Ideal para layouts onde é necessário 
     * que o usuário redimensione diferentes áreas de conteúdo.
     */
    
    /*
     * AnchorPane: 
     * Um layout que permite ancorar os filhos em diferentes bordas da 
     * área de visualização. Útil para criar interfaces onde os componentes 
     * devem se manter em posições fixas, mesmo quando a janela é redimensionada.
     */
    
    /*
     * BorderPane: 
     * Divide a área de visualização em cinco regiões (cima, baixo, esquerda, 
     * direita e centro). É ideal para criar layouts de aplicações com 
     * um cabeçalho, rodapé e conteúdo central.
     */
    
    /*
     * FlowPane: 
     * Organiza os componentes em uma linha ou coluna, permitindo que eles 
     * fluam para a próxima linha ou coluna quando não houver espaço 
     * suficiente. É ótimo para layouts responsivos que se ajustam ao 
     * tamanho da janela.
     */
    
    /*
     * GridPane: 
     * Permite organizar os componentes em uma grade, onde você pode especificar 
     * a posição exata de cada um. É ideal para formulários e layouts complexos 
     * que requerem alinhamento preciso.
     */
    
    /*
     * StackPane: 
     * Empilha os componentes uns sobre os outros, permitindo que você 
     * sobreponha elementos. Útil para criar efeitos visuais e layouts onde 
     * um componente deve ser destacado sobre os outros.
     */
    
    /*
     * TilePane: 
     * Organiza os componentes em células de tamanho fixo, em linhas e colunas. 
     * É útil para layouts que exigem espaçamento consistente entre os 
     * componentes, como botões ou imagens em um painel.
     */
}
