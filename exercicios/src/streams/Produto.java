package streams;

public class Produto {

	final String nome;
	final double preco;
	final double desconto;
	final int prazoEntrega;
	final boolean boaGarantia;
	
	public Produto(String nome, double preco, double desconto, int prazoEntrega, boolean boaGarantia) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.prazoEntrega = prazoEntrega;
		this.boaGarantia = boaGarantia;
	}
	
	
	
	
}
